package Operators;

public class ArithmeticDemo {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
        System.out.println((10 + 20) / 2);
        System.out.println(10 / (2 * 5));
    }
}
